import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const sourcePath = resolve("assets", "template_analysis.xmind");
const outputPath = resolve("src", "generated", "templateAnalysisXmind.ts");

const base64 = readFileSync(sourcePath).toString("base64");
const contents = `// Generated by scripts/generate-xmind-template.mjs. Do not edit manually.
export const TEMPLATE_ANALYSIS_XMIND_BASE64 = ${JSON.stringify(base64)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, contents, "utf8");

process.stdout.write(`Generated ${outputPath}\n`);
